/************************************************************************/
/* ＲＡＭエリアの初期化                                                 */
/* 引数　 なし                                                          */
/* 戻り値 なし                                                          */
/************************************************************************/
void INITSCT( void )
{
    int *s, *e, *r;

    r = __sectop("R");                  /* Rセクション(RAM)の最初   */
    s = __sectop("D");                  /* Dセクション(ROM)の最初   */
    e = __secend("D");                  /* Dセクション(ROM)の最後   */
    while(s < e) {
        *r++ = *s++;                    /* R ← D コピー            */
    }

    s = __sectop("B");                  /* Bセクション(RAM)の最初   */
    e = __secend("B");                  /* Bセクション(RAM)の最後   */
    while(s < e) {
        *s++ = 0x00;                    /* B ← 0x00                */
    }
}

